package swap_two_integer;

public class simple {
          public static void main(String args[]){
                    int a = 4;
                    int b = 2;
                    int temp; // most programmer use temp for temporary to swapping any two integer or any two string or etc.

                    temp = a;
                    a = b;
                    b = temp;
                    System.out.println("a= "+a);
                    System.out.println("b = " +b);
          }
}
